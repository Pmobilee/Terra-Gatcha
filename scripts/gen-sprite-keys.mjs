import { readdirSync, statSync, writeFileSync } from 'fs'
import { join, relative } from 'path'

function scan(dir, base) {
  const result = {}
  function walk(cur) {
    for (const entry of readdirSync(cur)) {
      const full = join(cur, entry)
      if (statSync(full).isDirectory()) {
        walk(full)
      } else if (entry.endsWith('.png')) {
        const key = entry.replace(/\.png$/, '')
        const rel = relative(base, full).replace(/\\/g, '/')
        result[key] = rel
      }
    }
  }
  walk(dir)
  return result
}

const high = scan('public/assets/sprites-hires', 'public/assets/sprites-hires')
const low = scan('public/assets/sprites', 'public/assets/sprites')

const content = `// AUTO-GENERATED by scripts/gen-sprite-keys.mjs — do not edit
// Run: node scripts/gen-sprite-keys.mjs

export const HIGH_RES_KEYS: Record<string, string> = ${JSON.stringify(high)};

export const LOW_RES_KEYS: Record<string, string> = ${JSON.stringify(low)};
`

writeFileSync('src/data/spriteKeys.ts', content)
console.log(`Generated spriteKeys.ts: ${Object.keys(high).length} hi-res, ${Object.keys(low).length} lo-res`)
